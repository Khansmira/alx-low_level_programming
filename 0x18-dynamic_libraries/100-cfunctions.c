#include "main.h"

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 on success and 1 otherwise
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 on success and 1 otherwise
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 on success 1 otherwise
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 on success and 1 otherwise
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulo of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: 0 on success and 1 otherwise.
 */

int mod(int a, int b)
{
	return (a % b);
}
